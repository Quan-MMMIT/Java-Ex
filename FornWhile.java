public class ForExample {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}

public class WhileExample1 {
 public static void main(String[] args) {
  int i = 1;
  while (i <= 10) {
   System.out.println(i);
   i++;
  }
 }
}
